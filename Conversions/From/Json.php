namespace Gears\Arrays\Conversions\From;

class Json extends \Gears\Arrays\Conversions\Template
{
	/**
	 * Method: Convert
	 * =========================================================================
	 * This will do the actual converting
	 * 
	 * Parameters:
	 * -------------------------------------------------------------------------
	 * $json - A JSON String to convert to array.
	 * 
	 * Returns:
	 * -------------------------------------------------------------------------
	 * array
	 */
	public function Convert($json)
	{
		// Convert the Json to PHP Array
		return json_decode($json, true);
	}
}
